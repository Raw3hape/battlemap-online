// Размер одного пикселя в км² (10км × 10км)
export const PIXEL_SIZE_KM2 = 100;

// Функция для получения максимального количества пикселей в стране
export function getMaxPixelsForCountry(countryCode) {
    const areaKm2 = COUNTRY_AREAS[countryCode] || COUNTRY_AREAS['XX'];
    return Math.floor(areaKm2 / PIXEL_SIZE_KM2);
}

// Функция для расчета процента заполнения страны
export function calculateCountryFillPercentage(pixelCount, countryCode) {
    const maxPixels = getMaxPixelsForCountry(countryCode);
    if (maxPixels === 0) return 0;
    
    // Возвращаем процент с точностью до 6 знаков после запятой
    // Например: 3 пикселя в России = 0.000018%
    const percentage = (pixelCount / maxPixels) * 100;
    return Math.round(percentage * 1000000) / 1000000; // 6 знаков после запятой
}

